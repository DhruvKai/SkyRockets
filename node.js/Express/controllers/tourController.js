exports.getAllTours = async (req, res) => {
  try {
    //BUILD QUERY
    //FILTERING
    const queryObj = { ...req.query };
    const excludeFields = ['page', 'sort', 'limit', 'fields'];
    excludeFields.forEach(el => delete queryObj[el]);
    //ADVANCED FILTERING
    let queryStr = JSON.stringify(queryObj); //reagular exp to add greater than and less than operators
    queryStr = queryStr.replace(/\b(gte|gt|lte|lt)\b/g, match => `${match}`);

    let query = Tour.find(JSON.parse(queryStr));

    //SORT
    if (req.query.sort) {
      const sortBy = req.query.sort.split(',').join(' ');
      query = query.sort(sortBy);
    } else {
      query = query.sort('-createdAt');
    }

    //FIELD LIMITING
    if (req.query.fileds) {
      const fields = req.query.split(',').join(' ');
      query = query.select('fields');
    } else {
      query = query.select('-__v'); //excludeing only this field
    }

    //Pagination
    const page = req.query.page * 1 || 1; //converting req.query.apge to number || by default page no 1
    const limit = req.query.limit * 1 || 100;
    const skip = (page - 1) * limit;
    //10 results per page
    query = query.skip(skip).limit(limit);
    if (req.query.page) {
      const numTour = await Tour.countDocuments();
      if (skip >= numTour) throw new Error('This page does not exist');
    }
    //execute query
    const tours = await query;
    // const tours = await Tour.find()
    //   .where('duration')
    //   .equals(5)
    //   .where('difficulty')
    //   .equals('easy');

    res.status(200).json({
      status: 'success',

      results: tours.length,
      data: {
        tours,
      },
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err,
    });
  }
};

exports.getTour = async (req, res) => {
  try {
    const tour = await Tour.findById(req.params.id);
    //Tour.findOne({__id: req.params.id}) the abose code is shorthand for this
    res.status(200).json({
      status: 'success',
      data: {
        tour,
      },
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err,
    });
  }
};

exports.createTour = async (req, res) => {
  try {
    const newTour = await Tour.create(req.body);

    res.status(201).json({
      status: 'success',
      data: {
        tour: newTour,
      },
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err,
    });
  }
};
exports.updateTour = async (req, res) => {
  try {
    const tour = await Tour.findByIdAndUpdate(req.params.id, req.body, {
      new: true,
      runValidators: true,
    });
    res.status(200).json({
      status: 'success',
      data: {
        tour,
      },
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err,
    });
  }
};

exports.deleteTour = async (req, res) => {
  try {
    await Tour.findByIdAndDelete(req.params.id);

    res.status(204).json({
      status: 'success',
      data: null,
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err,
    });
  }
};
